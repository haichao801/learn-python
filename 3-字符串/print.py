# print 函数用法
# print(value1,value2,...,sep=' ',end='\n')
# 1.这里的value输入一个或多个，则打印一个或多个
# 2.sep 代表可自定义value之间的符号，默认为空格
# 3.end 代表可自定义value结尾结尾的符号，默认为换行符号
print('lemon', 'apple', 'banana')
print('lemon', 'apple', 'banana', sep=',', end='.\n')


# 用加号进行字符串拼接，拼接的两部分之间没有空格
message = "你好，欢迎到来"
print("小李" + message)

# 用逗号打印，逗号前后两部分有空格
message = "你好，欢迎到来"
print("小李", message)



